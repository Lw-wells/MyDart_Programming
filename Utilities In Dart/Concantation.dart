// Concatenation: Concatenation combines two or more strings into one.
// Dart uses the + operator for this.

// Concatenation is useful for building strings dynamically, such as creating full names or messages.
// Concatenation is a fundamental operation in string manipulation.
void main() {
  String firstName = 'John';
  String lastName = 'Doe';

  // Concatenate two strings
  String fullName = firstName + ' ' + lastName;

  print('Full Name: $fullName'); // Output: Full Name: John Doe
}
